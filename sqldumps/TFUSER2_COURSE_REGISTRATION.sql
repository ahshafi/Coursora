create table COURSE_REGISTRATION
(
    ID          NUMBER        default "TFUSER2"."COURSE_REG_SEQ"."NEXTVAL" not null
        constraint COURSE_REGISTRATION_PK
            primary key,
    GRADE       VARCHAR2(10)  default NULL,
    CERTIFICATE VARCHAR2(100) default NULL,
    STUDENT_ID  NUMBER
        constraint COURSE_REGISTRATION_STUDENT__FK
            references "Student",
    COURSE_ID   NUMBER
        constraint COURSE_REGISTRATION_COURSE__FK
            references "Course"
)
